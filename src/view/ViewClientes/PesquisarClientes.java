    private void pesquisarBtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_pesquisarBtnActionPerformed

        ClientDAO dao;
        List<Client> clientes;
        String sql = "";
        DefaultTableModel table = (DefaultTableModel) pesquisaTable.getModel();
        table.setNumRows(0);

        if (rbId.isSelected()) {
            sql = "SELECT * from cliente WHERE id=" + pesquisaInput.getText();
        } else if (rbNome.isSelected()) {
            sql = "SELECT * from cliente WHERE nome LIKE '%" + pesquisaInput.getText() + "%'";
        } else if (rbCpf.isSelected()) {
            sql = "SELECT * from cliente WHERE cpf=" + pesquisaInput.getText();
        }

        try {
            dao = new ClientDAO();
            clientes = dao.findClients(sql);

            for (Client cliente : clientes) {
                Object[] clienteData = { cliente.getId(), cliente.getName(), cliente.getCpf(), cliente.getAddress(),
                        cliente.getEmail() };
                table.addRow(clienteData);
            }

            dao.close();
        } catch (SQLException e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }

    }// GEN-LAST:event_pesquisarBtnActionPerformed

