    private void ordernarControlItemStateChanged(java.awt.event.ItemEvent evt) {// GEN-FIRST:event_ordernarControlItemStateChanged
        String sql = "SELECT * from cliente";

        switch (ordernarControl.getSelectedItem().toString()) {
            case "ID":
                sql = "SELECT * from cliente ORDER by id";
                break;
            case "Nome":
                sql = "SELECT * from cliente ORDER by nome";
                break;
            default:
                break;
        }

        ClientDAO dao;
        List<Client> clientes;
        DefaultTableModel table = (DefaultTableModel) tableUsuarios.getModel();
        table.setNumRows(0);

        try {
            dao = new ClientDAO();
            clientes = dao.listClients(sql);
            String disponivel;

            for (Client cliente : clientes) {
                Object[] clienteData = { cliente.getId(), cliente.getName(), cliente.getCpf(), cliente.getAddress(),
                        cliente.getEmail() };
                table.addRow(clienteData);
            }
            ;
            dao.close();
        } catch (SQLException ex) {
            Logger.getLogger(ListarClientes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }// GEN-LAST:event_ordernarControlItemStateChanged

