    private void excluirBtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_excluirBtnActionPerformed
        UserDAO dao;

        try {
            dao = new UserDAO();

            if (dao.deleteUser(Integer.parseInt(labelId.getText()))) {
                JOptionPane.showMessageDialog(null, "Usuário excluído!");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Erro! Usuário não excluído, tente novamente!");
            }

            dao.close();
        } catch (SQLException e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }
    }// GEN-LAST:event_excluirBtnActionPerformed

    private void pesquisarBtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_pesquisarBtnActionPerformed
        UserDAO dao;
        User user;
        String sql = "";

        if (rbId.isSelected()) {
            sql = "SELECT * from usuario where id=?";
        } else if (rbCpf.isSelected()) {
            sql = "SELECT * from usuario where cpf=?";
            ;
        }

        try {
            dao = new UserDAO();
            user = dao.findUser(sql, pesquisaInput.getText());

            if (user != null) {
                labelId.setText(Integer.toString(user.getId()));
                labelNome.setText(user.getName());
                labelCpf.setText(user.getCpf());
                labelTelefone.setText(user.getPhone());
                labelEmail.setText(user.getEmail());
                labelLogin.setText(user.getUsername());
            } else {
                JOptionPane.showMessageDialog(null, "Usuário não encontrado!");
                labelId.setText("");
                labelNome.setText("");
                labelCpf.setText("");
                labelTelefone.setText("");
                labelEmail.setText("");
                labelLogin.setText("");
            }

            dao.close();
        } catch (SQLException e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }
    }// GEN-LAST:event_pesquisarBtnActionPerformed

