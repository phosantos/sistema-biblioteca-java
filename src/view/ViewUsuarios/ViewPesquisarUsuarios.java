    private void pesquisarBtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_pesquisarBtnActionPerformed

        UserDAO dao;
        List<User> users;
        String sql = "";
        DefaultTableModel table = (DefaultTableModel) pesquisaTable.getModel();
        table.setNumRows(0);

        if (rbId.isSelected()) {
            sql = "SELECT * from user WHERE id=" + pesquisaInput.getText();
        } else if (rbNome.isSelected()) {
            sql = "SELECT * from user WHERE nome LIKE '%" + pesquisaInput.getText() + "%'";
        } else if (rbCpf.isSelected()) {
            sql = "SELECT * from user WHERE cpf=" + pesquisaInput.getText();
        } else if (rbLogin.isSelected()) {
            sql = "SELECT * from user WHERE login LIKE '%" + pesquisaInput.getText() + "%'";
        }

        try {
            dao = new UserDAO();
            users = dao.findUsers(sql);

            for (User user : users) {
                Object[] userData = { user.getId(), user.getName(), user.getUsername(), user.getCpf() };
                table.addRow(userData);
            }

            dao.close();
        } catch (SQLException e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }

    }// GEN-LAST:event_pesquisarBtnActionPerformed

