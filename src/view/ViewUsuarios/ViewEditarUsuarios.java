    private void editarBtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_editarBtnActionPerformed
        UserDAO dao;
        User user = new User(Integer.parseInt(labelId.getText()), nomeInput.getText(), verificaAdmInput.isSelected(),
                loginInput.getText(), senhaInput.getText(), cpfInput.getText());

        try {
            dao = new UserDAO();

            if (dao.updateUser(user)) {
                JOptionPane.showMessageDialog(null, "Dados do usu√°rio atualizados!");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Erro! Dados n√£o atualizados, tente novamente!");
            }

            dao.close();
        } catch (SQLException e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }
    }// GEN-LAST:event_editarBtnActionPerformed

    private void pesquisarBtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_pesquisarBtnActionPerformed
        UserDAO dao;
        User user;
        String sql = "";

        if (rbId.isSelected()) {
            sql = "SELECT * from user where id=?";
        } else if (rbCpf.isSelected()) {
            sql = "SELECT * from user where cpf=?";
            ;
        }

        try {
            dao = new UserDAO();
            user = dao.findUser(sql, pesquisaInput.getText());

            if (user != null) {
                nomeInput.setText(user.getName());
                cpfInput.setText(user.getCpf());
                loginInput.setText(user.getUsername());
                labelId.setText(String.valueOf(user.getId()));
                verificaAdmInput.setSelected(user.getIsAdm());
            } else {
                JOptionPane.showMessageDialog(null, "Usu√°rio n√£o encontrado!");
                nomeInput.setText("");
                cpfInput.setText("");
                loginInput.setText("");
                labelId.setText("");
                verificaAdmInput.setSelected(false);
            }

            dao.close();
        } catch (SQLException e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }

    }// GEN-LAST:event_pesquisarBtnActionPerformed

